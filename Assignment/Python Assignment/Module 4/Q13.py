#Explain Exception handling? What is an Error in Python?
'''
Exception handling in Python is a way to deal with errors that occur during the execution of a program.
Instead of letting the program crash when something goes wrong, exception handling lets you catch the error,
manage it, and continue running the program smoothly.

In Python, an error is an issue that occurs during the execution of a program, causing the program to stop or
behave unexpectedly. Errors can be categorized into different types, each representing a specific problem.

#Some of the common types of errors in Python 
SyntaxError: Incorrect syntax in the code.
ZeroDivisionError: Division by zero.
FileNotFoundError: File not found.
ValueError: Incorrect value type.
TypeError: Operation on inappropriate type.
IndexError: Index out of range in a sequence.
KeyError: Key not found in a dictionary.
NameError: Name not defined.
ImportError: Module import failure.
NotImplementedError: Abstract method not implemented.
RuntimeError: General runtime error.

''' 
