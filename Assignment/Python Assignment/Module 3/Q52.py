#How Many Basic Types Of Functions Are Available In Python?
'''
Basic Function Definition
Definition: A standard function with parameters and a return value.

Functions with Default Parameters
Definition: Functions that have default values for some parameters.

Positional Parameters
Definition: Functions that require parameters in a specific order.

Variable-Length Arguments
Definition: Functions that accept a variable number of arguments, using *args and **kwargs.

Global and Local Variables
Definition: Functions that can modify global variables using the global keyword.

Lambda Functions
Definition: Anonymous functions defined with the lambda keyword.

Decorators
Definition: A specific type of higher-order function used to modify or enhance other functions.

Functional Programming Concepts
Definition: Functions that use techniques like map and filter with lambda functions.'''