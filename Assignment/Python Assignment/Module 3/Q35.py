#How Do You Traverse Through A Dictionary Object In Python?
'''
There are many ways to Tranverse through Dictionay Object using 'for' loop some of them are below:
1. Traverse through Keys
2. Traverse through Values
3. Traverse through Key-Value Pairs
4. Traverse through Keys and Access Corresponding Values
'''
#Example of Traverse through Keys
my_dict = {'name': 'Neel', 'age': 20, 'city': 'Surat'}
for key in my_dict:
    print(key)

