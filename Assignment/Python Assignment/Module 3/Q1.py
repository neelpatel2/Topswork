# What is List? How will you reverse a list?
'''A list in Python is a collection of items that are ordered and changeable. It allows duplicate members and 
can contain items of different types, such as integers, floats, strings, or even other lists.

You can reverse the string using the following methods:-
1) Using slicing: This creates a new list that is the reverse of the original
#example
my_list = [1, 2, 3, 4, 5]
reversed_list = my_list[::-1]
print(reversed_list)  
# Output: [5, 4, 3, 2, 1]

2)Using the reverse() method: This method reverses the list in place.
#example
my_list = [1, 2, 3, 4, 5]
my_list.reverse()
print(my_list) 
# Output: [5, 4, 3, 2, 1]
'''

