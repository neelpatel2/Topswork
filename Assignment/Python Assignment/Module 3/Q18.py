# What is tuple? Difference between list and tuple.
'''A tuple is a collection of objects which is ordered and immutable. Tuples are similar to lists, 
but the main difference is that the elements of a tuple cannot be changed after it is assigned.
Tuples use parentheses, whereas lists use square brackets.
Difference between Tuple and List 
Tuple:
Ordered: Elements have a defined order.
Immutable: Once a tuple is created, its elements cannot be modified, added, or removed.
Heterogeneous: Can contain elements of different types.
Defined using parentheses ().
Typically used for fixed collections of items.
List:
Ordered: Elements have a defined order.
Mutable: Elements can be changed, and you can add or remove elements.
Heterogeneous: Can contain elements of different types.
Defined using square brackets [].
Typically used for collections of items that may change over time.
'''

